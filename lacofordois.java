
public class lacofordois {
    public static void main(String[] args) {

        int numero; // variável de controle
        int quadrado; // armazena o quadrado

        for (numero = 15; numero <= 100; numero++) {
            quadrado = numero * numero; // calcula o quadrado
            System.out.println("O quadrado de " + numero + " é " + quadrado);
        }

    }
}